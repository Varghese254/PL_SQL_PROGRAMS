

DECLARE
  ACC_BAL NUMBER(10, 2);
 ACCOUNT_NUM NUMBER(10,2);
BEGIN
  DBMS_OUTPUT.PUT_LINE('ENTER ACCOUNT NUMBER');
  ACCOUNT_NUM:=&ACCOUNT_NUM;
  SELECT ACC_BALANCE INTO ACC_BAL FROM ACCOUNTS WHERE ACC_NUMBER =ACCOUNT_NUM;

  IF ACC_BAL >= 2500 THEN
    
    UPDATE ACCOUNTS SET ACC_BALANCE = ACC_BAL - 2000 WHERE ACC_NUMBER =ACCOUNT_NUM;
    
    COMMIT;
    DBMS_OUTPUT.PUT_LINE('Rs. 2000 debited');
  ELSE
    DBMS_OUTPUT.PUT_LINE('Insufficient balance');
  END IF;
EXCEPTION
  WHEN NO_DATA_FOUND THEN
    DBMS_OUTPUT.PUT_LINE('Account not found.');
END;
/
