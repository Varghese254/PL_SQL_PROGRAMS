

DECLARE
  INVERTED_NUM NUMBER:=0;
  REMAINDER NUMBER;
  TEMP_NUMBER NUMBER := &ORIGINAL_NUM; 
BEGIN
  WHILE TEMP_NUMBER > 0 LOOP
   REMAINDER := TEMP_NUMBER - FLOOR(TEMP_NUMBER / 10) * 10;
    INVERTED_NUM := INVERTED_NUM * 10 + REMAINDER;
    TEMP_NUMBER := (TEMP_NUMBER - REMAINDER) / 10;
  END LOOP;

  DBMS_OUTPUT.PUT_LINE('INVERTED NUMBER IS ' || INVERTED_NUM);
END;
/
